package it.unibo.towerdefense.models.engine;

/**
 * Interface that models the concept of size
 * (as a rectangle) in a 2D space.
 */
public interface Size {

    /**
     * Width getter.
     * @return the width
     */
    int getWidth();

    /**
     * Height getter.
     * @return the height
     */
    int getHeight();
}
